//
// CABAC context model initialization for I-slices (ctxIdx 0..275).
//
// Initialization procedure (H.264 / ISO/IEC 14496-10):
// preCtxState = Clip3(1, 126, ((m * QP) >> 4) + n)
// if preCtxState <= 63: pStateIdx = 63 - preCtxState, valMPS = 0
// else:                pStateIdx = preCtxState - 64, valMPS = 1

use crate::cabac::CtxState;

pub struct CabacModels {
    ctx: [CtxState; 276],
}

impl CabacModels {
    pub fn new_i_slice(qp_y: i32) -> Self {
        let qp = qp_y.clamp(0, 51);

        let ctx = core::array::from_fn(|i| {
            let (m, n) = MN_I[i];
            init_ctx(m as i32, n as i32, qp)
        });

        Self { ctx }
    }

    #[inline]
    pub fn ctx_mut(&mut self, ctx_idx: usize) -> &mut CtxState {
        &mut self.ctx[ctx_idx]
    }
}

#[inline]
fn init_ctx(m: i32, n: i32, qp: i32) -> CtxState {
    let pre = (((m * qp) >> 4) + n).clamp(1, 126);

    if pre <= 63 {
        CtxState {
            p_state_idx: (63 - pre) as u8,
            val_mps: 0,
        }
    } else {
        CtxState {
            p_state_idx: (pre - 64) as u8,
            val_mps: 1,
        }
    }
}

